   <h2>Firefly and Serenity</h2>

   <p>My inspiration for the space travel calculator was the television series <a href="http://www.imdb.com/title/tt0303461/">Firefly</a> and its companion movie, <a href="http://www.imdb.com/title/tt0379786/">Serenity</a>. They're great entertainment and even the science isn't bad, especially by Hollywood standards. It's one of the few Hollywood movies to show that there's no sound in space. There are no aliens and the humans live on terraformed planets. The show's dubious handling of gravity is easily forgiven since it was a relatively low-budget affair and it's hard to film people floating about spacecrafts. Nevertheless, there's a big scientific problem in the storyline: how did millions of people
migrate from earth to a planet-filled sun-system a mere 500 years from
now, and have the time and resources to terraform so many planets?</p>

<p>
Let's assume that this wonderful system is Proxima Centauri, our nearest stellar neigbour. Being a red dwarf, Proxima Centauri is not a good candidate for livable planets, but let's ignore that.  
</p>

<p>
   Proxima Centauri is 4.2 light-years away. Let's say the first generation of humans to leave earth came in spacecrafts the size of the Firefly carrying young people prepared to spend most of the rest of their lives on a spaceship. If they'd travelled at a constant acceleration of 1g or 9.8m/s<sup>2</sup> for the first half of the journey, followed by the same constant deceleration, they would have got there in just three and a half years. This is a pleasant rate of acceleration because it would allow them to experience gravity for the duration of their journey as it is on earth. </p>

<p>The problem though is that they would have needed to carry far more fuel than the maximum capacity of their spacecrafts. In fact no matter what the mass of their spacecraft, they would have needed much more fuel than they could carry to get there in such a speedy time. Even the most efficient spacecraft, that convert their fuel mass into energy at the rate of e=mc<sup>2</sup> (e.g. by using  matter-antimatter annihilation), would always need more than four times as much fuel as the total mass of the spacecraft (including fuel) to maintain that acceleration, which is impossible. As it is, the most likely interstellar fuel mass into energy efficiency we're likely to achieve is only 0.008mc<sup>2</sup> (hydrogen fusion into helium). 
</p>

<p>Methods of powering spacecraft for interstellar flight without onboard fuel have been postulated. They're quite unlikely though. More importantly, there are scenes in the series which make it clear that the technology of the day depends on onboard fuel. 
</p>

<p>
Let's say our earth emigrants are prepared to tolerate an extremely long journey in space and no gravity for a long period of time. Then what's a realistically achievable amount of time to get from earth to Proxima Centauri? Well, about 90 odd years, assuming that there are some tremendous technological developments in the next few centuries. In this scenario, almost all the weight of the spacecrafts would be taken by fuel, at least for a large part of the first half of the journey. The crafts would be powered with nuclear fusion. They would accelerate for half the journey at a stately 0.017m/s<sup>2</sup> and then decelerate at the same rate until the destination was reached. 
</p>

<p>
   It is conceivable that we'll be able to live extremely long lives or that living in stasis will become possible. We haven't even considered other massive technological challenges like terraforming planets, finding a star with so many planets that have an earth-like climate and gravitational pull, ensuring there's enough food to eat on the spacecrafts for so long a journey and much, much more. But the state of humanity depicted in the Firefly series, while improbable, is perhaps not impossible a very long time into the future.
</p>

<h2>Star Trek</h2>

<p> The <a href="http://www.startrek.com/">Star Trek series</a> are
wonderful entertainment and offer a multitude of interesting scientific
and logical conundrums, but the space travel science in all the series,
in fact nearly every episode, is just awful. None of the characters,
none of the spaceships, no-one and nothing experiences time dilation. That is with
the exception of the confusing episodes which specifically deal with
time travel; those ones are particularly confusing.  </p>

<p>So the Enterprise and Voyager travel at warp speed, an impossibility,
across the galaxy, as do Klingons, Vulcans and other humanoids. Yet when
they return to their home planets or meet comrades on different ships
multiple times across different episodes, their age differences remain
identical.  It's just not possible, unless Einstein got it all wrong.</p>

